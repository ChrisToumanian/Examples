#include "math.h"

double Math::Add(double a, double b)
{
    return a + b;
}

double Math::Subtract(double a, double b)
{
    return a - b;
}

double Math::Multiply(double a, double b)
{
    return a * b;
}

double Math::Divide(double numerator, double denominator)
{
    return numerator / denominator;
}
